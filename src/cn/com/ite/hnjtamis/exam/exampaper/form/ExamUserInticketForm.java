package cn.com.ite.hnjtamis.exam.exampaper.form;

public class ExamUserInticketForm {

	
	private String inticket;//准考证
	private String examPassword;//准考证密码
	private String employee_id;//员工ID
	private String account;//登录账户
	private String password;//登录密码
	private String examId;//考试信息ID
	private String identityCard;//身份证号
	private String examName;//考试信息
	
	public String getInticket() {
		return inticket;
	}
	public void setInticket(String inticket) {
		this.inticket = inticket;
	}
	public String getExamPassword() {
		return examPassword;
	}
	public void setExamPassword(String examPassword) {
		this.examPassword = examPassword;
	}
	public String getEmployee_id() {
		return employee_id;
	}
	public void setEmployee_id(String employee_id) {
		this.employee_id = employee_id;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getExamId() {
		return examId;
	}
	public void setExamId(String examId) {
		this.examId = examId;
	}
	public String getIdentityCard() {
		return identityCard;
	}
	public void setIdentityCard(String identityCard) {
		this.identityCard = identityCard;
	}
	public String getExamName() {
		return examName;
	}
	public void setExamName(String examName) {
		this.examName = examName;
	}
	
	
}
